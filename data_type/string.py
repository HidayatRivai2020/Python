# String declaration with different quotes
single_quotes = 'this is single quotes text'
print(single_quotes)

double_quotes = "this is double quotes text"
print(double_quotes)

# String indexing and slicing
print("this is the index 0 of double_quotes:" + double_quotes[0])
print("this is the index 5 of double_quotes:" + double_quotes[5])
print("this is from index 5 until before index 15 of double_quotes:" + double_quotes[5:15])

# Escape characters
print('this is escape character: \', \\, \n, \t, \" ')