print("=== None ===")

# None variable declaration
none_var = None
print(none_var)  # prints "None"

# Checking if variable is None
if none_var is None:
    print("The variable is None")
else:
    print("The variable is not None")

# Assigning a value to the None variable
none_var = 10
print(none_var)  # prints "10"