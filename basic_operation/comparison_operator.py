print("1 < 5 --> {}".format(1 < 5))
print("1 <= 5 --> {}".format(1 <= 5))
print("1 <= 1 --> {}".format(1 <= 1))

print("1 > 5 --> {}".format(1 > 5))
print("1 >= 5 --> {}".format(1 >= 5))
print("1 >= 1 --> {}".format(1 >= 1))

print("1 == 5 --> {}".format(1 == 5))
print("1 == 1 --> {}".format(1 == 1))
print("1 != 1 --> {}".format(1 != 1))
print("1 != 5 --> {}".format(1 != 5))

print("a == a --> {}".format('a' == 'a'))
print("a == b --> {}".format('a' == 'b'))
print("a != a --> {}".format('a' != 'a'))
print("a != b --> {}".format('a' != 'b'))