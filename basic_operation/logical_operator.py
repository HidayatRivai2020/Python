print("=== Logical Operator ===")

# Logical operators: and, or, not
print("1 < 5 and 2 < 6 --> {}".format(1 < 5 and 2 < 6))
print("1 < 5 and 2 > 6 --> {}".format(1 < 5 and 2 > 6))
print("1 > 5 and 2 > 6 --> {}".format(1 > 5 and 2 > 6))

print("1 < 5 or 2 < 6 --> {}".format(1 < 5 or 2 < 6))
print("1 < 5 or 2 > 6 --> {}".format(1 < 5 or 2 > 6))
print("1 > 5 or 2 > 6 --> {}".format(1 > 5 or 2 > 6))