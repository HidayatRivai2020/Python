print("=== Loop Statement ===")

# Pass statement (placeholder)
for a in range(1, 5):
    pass

print("----")

# Break statement
for a in range(1, 5):
    print(a)
    if a == 3:
        print("break")

print("----")

# Continue statement
for a in range(1, 5):
    print(a)
    if a == 3:
        print("continue")
